interface Repo {
  name: string;
  stargazers_count: number;
}

interface Language {
  code: string;
  flag: string;
  name: string;
}
const LanguageSelector = () => {
 const { lang } = useParams<{ lang: string }>();
  const [language, setLanguage] = useState(lang || "es");
  const [isOpen, setIsOpen] = useState(false);

  // Obtenemos los idiomas usando LenguajeDevolvedor.
  const languagesRaw = LenguajeDevolvedor(undefined, "languaje");
  // Si languagesRaw es un string (JSON), lo parseamos; de lo contrario, lo usamos directamente.
  const languages: Language[] =
    typeof languagesRaw === "string" ? JSON.parse(languagesRaw) : languagesRaw;

  const handleLanguageChange = (code: string): void => {
    setLanguage(code);
    setIsOpen(false);
    window.location.href = `/Documentacionsegmentslibrary/${code}/home`;
  };

  const selectedLanguage = languages.find((l) => l.code === language);

  return (
    <div className="relative w-48">
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="w-full bg-gray-700 text-white px-4 py-2 rounded-xl flex items-center gap-2 hover:bg-gray-600 transition-colors"
      >
        <img
          src={selectedLanguage?.flag}
          alt=""
          className="w-6 h-6 rounded-sm object-cover"
        />
        <span>{selectedLanguage?.name}</span>
        <span className="ml-auto">▼</span>
      </button>

      {isOpen && (
        <div className="absolute mt-2 w-full bg-gray-700 rounded-xl shadow-lg overflow-hidden z-50">
          {languages.map(({ code, flag, name }) => (
            <button
              key={code}
              onClick={() => handleLanguageChange(code)}
              className={`w-full px-4 py-2 flex items-center gap-2 hover:bg-gray-600 transition-colors ${
                code === language ? "bg-gray-600" : ""
              }`}
            >
              <img
                src={flag}
                alt=""
                className="w-6 h-6 rounded-sm object-cover"
              />
              <span className="text-white">{name}</span>
            </button>
          ))}
        </div>
      )}
    </div>
  );
};

const SegmentsLibraryStars = () => {
  const [stars, setStars] = useState<number | null>(null);

  useEffect(() => {
    fetch("https://api.github.com/users/luis-huaney-lino-devops/repos")
      .then((res) => res.json())
      .then((repos: Repo[]) => {
        const repo = repos.find((r) => r.name === "SegmentsLibrary");
        if (repo) {
          setStars(repo.stargazers_count);
        } else {
          console.error("Repositorio SegmentsLibrary no encontrado");
        }
      })
      .catch((error) => console.error("Error al consumir la API", error));
  }, []);

  return (
    <>
      {stars !== null ? (
        <span className="absolute group-hover:animate-fade-out-up">
          {stars} ⭐️ en GitHub
        </span>
      ) : (
        <Loader/>
      )}
    </>
  );
};

